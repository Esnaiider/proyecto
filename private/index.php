function getContenido($data){
    $code = "200";
    $contenido = $data['atr_contenido'];
    $html = "";
    $titulo_general = "";
    
    if ($contenido == 'ingresarContacto') {
        $titulo_general = $data['titulo_general'];
        $html = viewContactos::form_ingreso();
    }

    if($contenido == 'listar_contactos'){
      $titulo_general = $data['titulo_general'];
      $html = viewContactos::Contactos();
    }

    $retorno = json_encode(
        array(
            "code" => $code,
            "titulo" => $titulo_general,
            "html" => rawurlencode(utf8_encode($html)),
        )
    );

    if ($html == "") {
        $retorno = json_encode(
        array(
            "code" => "404"
        )
    );
        
    }

    return $retorno;
}
